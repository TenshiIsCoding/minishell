#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*ptr;
	int				len;

	if (s == NULL || f == NULL)
		return (0);
	i = 0;
	len = ft_strlen(s) + 1;
	ptr = malloc((sizeof(char)) * len);
	if (!ptr)
		return (0);
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
